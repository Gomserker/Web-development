package com.bg.pay;

public class Remain {
	private int i;
	private int e;
	private int result;
	
	public Remain() {}
	
	public Remain(int i, int e, int result) {
		super();
		this.i = i;
		this.e = e;
		this.result = result;
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getE() {
		return e;
	}
	public void setE(int e) {
		this.e = e;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	
	
}
